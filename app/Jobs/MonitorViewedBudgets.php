<?php

namespace App\Jobs;

use App\Models\Notification;
use App\Models\NotificationSetting;
use App\Models\Orcamento;
use Illuminate\Bus\Queueable;
use Illuminate\Contracts\Queue\ShouldQueue;
use Illuminate\Foundation\Bus\Dispatchable;
use Illuminate\Queue\InteractsWithQueue;
use Illuminate\Queue\SerializesModels;
use Illuminate\Support\Facades\Log;
use Carbon\Carbon;

class MonitorViewedBudgets implements ShouldQueue
{
    use Dispatchable, InteractsWithQueue, Queueable, SerializesModels;

    public $timeout = 300; // 5 minutos
    public $tries = 3;
    public $backoff = [60, 120, 300]; // 1min, 2min, 5min

    /**
     * Execute the job.
     */
    public function handle(): void
    {
        Log::info('MonitorViewedBudgets: Iniciando monitoramento de or√ßamentos visualizados');

        try {
            $this->checkViewedBudgetsWithoutAction();
            $this->checkFrequentlyViewedBudgets();
            
            Log::info('MonitorViewedBudgets: Monitoramento conclu√≠do com sucesso');
        } catch (\Exception $e) {
            Log::error('MonitorViewedBudgets: Erro durante monitoramento', [
                'error' => $e->getMessage(),
                'trace' => $e->getTraceAsString()
            ]);
            throw $e;
        }
    }

    /**
     * Verificar or√ßamentos visualizados sem a√ß√£o
     */
    private function checkViewedBudgetsWithoutAction(): void
    {
        // Buscar configura√ß√µes de usu√°rios ativos
        $settings = NotificationSetting::with('user')
            ->whereHas('user', function ($query) {
                $query->whereNotNull('email_verified_at');
            })
            ->get();

        foreach ($settings as $setting) {
            $this->checkUserViewedBudgets($setting);
        }
    }

    /**
     * Verificar or√ßamentos visualizados para um usu√°rio espec√≠fico
     */
    private function checkUserViewedBudgets(NotificationSetting $setting): void
    {
        $userId = $setting->user_id;
        $daysAfterView = $setting->days_after_view;
        
        // Data limite para notifica√ß√£o (or√ßamentos visualizados h√° X dias)
        $viewThreshold = now()->subDays($daysAfterView);
        
        // Buscar or√ßamentos visualizados sem a√ß√£o
        $budgets = Orcamento::where(function ($query) use ($userId) {
                // Or√ßamentos do usu√°rio ou que ele pode visualizar
                $query->where('user_id', $userId)
                      ->orWhere('created_by', $userId);
            })
            ->where('status', 'pendente') // Apenas or√ßamentos pendentes
            ->whereNotNull('first_viewed_at')
            ->where('first_viewed_at', '<=', $viewThreshold)
            ->where(function ($query) {
                // Sem aprova√ß√£o ou rejei√ß√£o recente
                $query->whereNull('approved_at')
                      ->whereNull('rejected_at');
            })
            ->with(['cliente', 'user'])
            ->get();

        foreach ($budgets as $budget) {
            $this->createViewedWithoutActionNotification($budget, $setting);
        }
    }

    /**
     * Criar notifica√ß√£o para or√ßamento visualizado sem a√ß√£o
     */
    private function createViewedWithoutActionNotification(Orcamento $budget, NotificationSetting $setting): void
    {
        // Verificar se j√° existe notifica√ß√£o recente para este or√ßamento
        $existingNotification = Notification::where('user_id', $setting->user_id)
            ->where('budget_id', $budget->id)
            ->where('type', 'viewed_no_action')
            ->where('created_at', '>=', now()->subDays(3)) // Evitar spam
            ->first();

        if ($existingNotification) {
            return; // J√° existe notifica√ß√£o recente
        }

        // Verificar se o tipo est√° habilitado nas configura√ß√µes
        if (!in_array('viewed_no_action', $setting->enabled_types)) {
            return;
        }

        $daysSinceViewed = now()->diffInDays($budget->first_viewed_at);
        $viewCount = $budget->view_count ?? 1;
        
        // Determinar prioridade baseada no tempo e n√∫mero de visualiza√ß√µes
        $priority = $this->calculateViewedPriority($daysSinceViewed, $viewCount, $budget->valor_total);
        
        // Criar mensagem personalizada
        $message = $this->createViewedMessage($budget, $daysSinceViewed, $viewCount);

        DashboardNotification::create([
            'user_id' => $setting->user_id,
            'budget_id' => $budget->id,
            'type' => 'viewed_no_action',
            'title' => 'Or√ßamento aguardando a√ß√£o',
            'message' => $message,
            'priority' => $priority,
            'metadata' => [
                'days_since_viewed' => $daysSinceViewed,
                'view_count' => $viewCount,
                'first_viewed_at' => $budget->first_viewed_at->toISOString(),
                'last_viewed_at' => $budget->last_viewed_at?->toISOString(),
                'budget_value' => $budget->valor_total,
                'client_name' => $budget->cliente->nome ?? 'Cliente n√£o informado',
                'auto_generated' => true,
                'notification_trigger' => 'viewed_monitor'
            ]
        ]);

        Log::info('MonitorViewedBudgets: Notifica√ß√£o de or√ßamento sem a√ß√£o criada', [
            'user_id' => $setting->user_id,
            'budget_id' => $budget->id,
            'days_since_viewed' => $daysSinceViewed,
            'view_count' => $viewCount,
            'priority' => $priority
        ]);
    }

    /**
     * Verificar or√ßamentos visualizados com muita frequ√™ncia
     */
    private function checkFrequentlyViewedBudgets(): void
    {
        // Buscar or√ßamentos com muitas visualiza√ß√µes mas sem a√ß√£o
        $frequentlyViewed = Orcamento::where('status', 'pendente')
            ->where('view_count', '>=', 10) // 10 ou mais visualiza√ß√µes
            ->whereNotNull('first_viewed_at')
            ->where('first_viewed_at', '<=', now()->subDays(2)) // Pelo menos 2 dias desde primeira visualiza√ß√£o
            ->whereNull('approved_at')
            ->whereNull('rejected_at')
            ->with(['cliente', 'user'])
            ->get();

        foreach ($frequentlyViewed as $budget) {
            $this->createFrequentlyViewedNotification($budget);
        }
    }

    /**
     * Criar notifica√ß√£o para or√ßamento muito visualizado
     */
    private function createFrequentlyViewedNotification(Orcamento $budget): void
    {
        // Verificar se j√° existe notifica√ß√£o recente
        $existingNotification = DashboardNotification::where('budget_id', $budget->id)
            ->where('type', 'frequently_viewed')
            ->where('created_at', '>=', now()->subWeek())
            ->first();

        if ($existingNotification) {
            return;
        }

        // Notificar usu√°rio respons√°vel e criador
        $usersToNotify = collect([$budget->user_id, $budget->created_by])
            ->filter()
            ->unique();

        foreach ($usersToNotify as $userId) {
            // Verificar configura√ß√µes do usu√°rio
            $setting = NotificationSetting::getForUser($userId);
            if (!in_array('frequently_viewed', $setting->enabled_types ?? [])) {
                continue;
            }

            Notification::create([
                'user_id' => $userId,
                'budget_id' => $budget->id,
                'type' => 'frequently_viewed',
                'title' => 'Or√ßamento muito visualizado',
                'message' => $this->createFrequentlyViewedMessage($budget),
                'priority' => 'medium',
                'metadata' => [
                    'view_count' => $budget->view_count,
                    'days_since_first_view' => now()->diffInDays($budget->first_viewed_at),
                    'first_viewed_at' => $budget->first_viewed_at->toISOString(),
                    'last_viewed_at' => $budget->last_viewed_at?->toISOString(),
                    'budget_value' => $budget->valor_total,
                    'client_name' => $budget->cliente->nome ?? 'Cliente n√£o informado',
                    'auto_generated' => true,
                    'notification_trigger' => 'frequent_view_monitor'
                ]
            ]);
        }

        Log::info('MonitorViewedBudgets: Notifica√ß√£o de or√ßamento muito visualizado criada', [
            'budget_id' => $budget->id,
            'view_count' => $budget->view_count,
            'users_notified' => $usersToNotify->toArray()
        ]);
    }

    /**
     * Calcular prioridade baseada no tempo e visualiza√ß√µes
     */
    private function calculateViewedPriority(int $daysSinceViewed, int $viewCount, float $budgetValue): string
    {
        // Or√ßamentos de alto valor t√™m prioridade maior
        $isHighValue = $budgetValue >= 10000;
        
        // Muitas visualiza√ß√µes indicam interesse
        $isFrequentlyViewed = $viewCount >= 5;
        
        // Muito tempo sem a√ß√£o
        $isOld = $daysSinceViewed >= 7;
        
        if ($isHighValue && ($isFrequentlyViewed || $isOld)) {
            return 'high';
        }
        
        if ($isFrequentlyViewed || $daysSinceViewed >= 5) {
            return 'medium';
        }
        
        return 'low';
    }

    /**
     * Criar mensagem para or√ßamento visualizado sem a√ß√£o
     */
    private function createViewedMessage(Orcamento $budget, int $daysSinceViewed, int $viewCount): string
    {
        $clientName = $budget->cliente->nome ?? 'Cliente n√£o informado';
        $budgetValue = 'R$ ' . number_format($budget->valor_total, 2, ',', '.');
        
        $viewText = $viewCount > 1 ? "{$viewCount} visualiza√ß√µes" : "1 visualiza√ß√£o";
        
        if ($daysSinceViewed >= 7) {
            return "üìã Or√ßamento #{$budget->id} foi visualizado h√° {$daysSinceViewed} dias ({$viewText}) mas ainda n√£o foi aprovado/rejeitado. Cliente: {$clientName} - Valor: {$budgetValue}";
        } else {
            return "üëÄ Or√ßamento #{$budget->id} com {$viewText} h√° {$daysSinceViewed} dias aguarda decis√£o. Cliente: {$clientName} - Valor: {$budgetValue}";
        }
    }

    /**
     * Criar mensagem para or√ßamento muito visualizado
     */
    private function createFrequentlyViewedMessage(Orcamento $budget): string
    {
        $clientName = $budget->cliente->nome ?? 'Cliente n√£o informado';
        $budgetValue = 'R$ ' . number_format($budget->valor_total, 2, ',', '.');
        $daysSinceFirst = now()->diffInDays($budget->first_viewed_at);
        
        return "üîç Or√ßamento #{$budget->id} foi visualizado {$budget->view_count} vezes em {$daysSinceFirst} dias, mas ainda n√£o foi decidido. Pode precisar de aten√ß√£o especial. Cliente: {$clientName} - Valor: {$budgetValue}";
    }

    /**
     * Handle a job failure.
     */
    public function failed(\Throwable $exception): void
    {
        Log::error('MonitorViewedBudgets: Job falhou ap√≥s todas as tentativas', [
            'error' => $exception->getMessage(),
            'trace' => $exception->getTraceAsString()
        ]);
    }
}